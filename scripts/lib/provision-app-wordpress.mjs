/**
 * Native helper for the intent-based WordPress installer.
 *
 * Responsibilities:
 *   1. Resolve domain → unix user + home directory.
 *   2. Ensure WordPress files exist under <home>/public_html
 *      (reuses install-app-wordpress.sh — curl + tar).
 *   3. Generate strong WordPress SALTs server-side (never via argv) and
 *      write wp-config.php with the supplied DB credentials.
 *   4. Set ownership/permissions so the unix user (and PHP-FPM running
 *      under that user) can read+execute everything.
 *
 * DB creation is NOT done here — the TS orchestrator handles that via
 * the existing db-create subcommand before calling us.
 *
 * SECURITY: dbPass is passed via argv, which is visible in `ps` for a
 * brief moment. This is the same risk profile as the existing db-create
 * subcommand. SALTs are generated locally and never appear in argv.
 */

import { execFile } from "node:child_process";
import { promisify } from "node:util";
import { randomBytes } from "node:crypto";
import path from "node:path";
import { access, mkdir, writeFile, stat, chmod } from "node:fs/promises";
import {
  emit,
  fail,
  resolveDomainUser,
  QADBAK_DIR,
} from "./provisioning-common.mjs";
import { jstep, jinfo } from "./journal-emit.mjs";
import { escapePhpSingleQuoted } from "./security-utils.mjs";

const exec = promisify(execFile);

function generateSalt() {
  // 64 random bytes encoded base64 = 88 chars. Trim + filter to ASCII
  // printable to match the wp-config.php format conventions.
  return randomBytes(64)
    .toString("base64")
    .replace(/[+/=]/g, (c) => ({ "+": "-", "/": "_", "=": "" })[c])
    .slice(0, 64);
}

function wpConfigSnippet({ dbName, dbUser, dbPass, tablePrefix }) {
  const saltKeys = [
    "AUTH_KEY",
    "SECURE_AUTH_KEY",
    "LOGGED_IN_KEY",
    "NONCE_KEY",
    "AUTH_SALT",
    "SECURE_AUTH_SALT",
    "LOGGED_IN_SALT",
    "NONCE_SALT",
  ];
  const salts = saltKeys
    .map((k) => `define( '${k}', '${generateSalt()}' );`)
    .join("\n");
  const esc = escapePhpSingleQuoted;
  return `<?php
/**
 * The base configuration for WordPress — generated by Qadbak.
 * If you re-run the installer, this file will be overwritten with new
 * SALTs (logging every active user out). To rotate SALTs intentionally
 * just delete this file and run the installer again.
 */

define( 'DB_NAME',     '${esc(dbName)}' );
define( 'DB_USER',     '${esc(dbUser)}' );
define( 'DB_PASSWORD', '${esc(dbPass)}' );
define( 'DB_HOST',     'localhost' );
define( 'DB_CHARSET',  'utf8mb4' );
define( 'DB_COLLATE',  '' );

${salts}

$table_prefix = '${esc(tablePrefix)}';

define( 'WP_DEBUG', false );
define( 'WP_AUTO_UPDATE_CORE', 'minor' );
define( 'DISALLOW_FILE_EDIT', true );
define( 'FS_METHOD', 'direct' );

if ( ! defined( 'ABSPATH' ) ) {
\tdefine( 'ABSPATH', __DIR__ . '/' );
}

require_once ABSPATH . 'wp-settings.php';
`;
}

async function ensureWordpressFiles(home, user) {
  const target = path.join(home, "public_html");
  await mkdir(target, { recursive: true });
  const marker = path.join(target, "wp-load.php");
  let alreadyInstalled = false;
  try {
    await access(marker);
    alreadyInstalled = true;
  } catch {
    /* not installed yet */
  }
  if (alreadyInstalled) {
    jinfo(`WordPress files already present at ${target} — skipping download.`);
    return target;
  }
  const installer = path.join(QADBAK_DIR, "scripts", "lib", "install-app-wordpress.sh");
  await exec("sudo", ["-u", user, "bash", installer, home, "public_html"], {
    timeout: 600_000,
    maxBuffer: 16 * 1024 * 1024,
  });
  jstep("shell", `Downloaded WordPress to ${target}`, {
    command: `sudo -u ${user} bash install-app-wordpress.sh ${home} public_html`,
  });
  return target;
}

export async function appInstallWordpress(domain, dbName, dbUser, dbPass, tablePrefix) {
  if (!domain || !dbName || !dbUser || !dbPass) {
    fail("appInstallWordpress requires: domain dbName dbUser dbPass");
  }
  const { user, home } = await resolveDomainUser(domain);
  jinfo(`Resolved domain ${domain} → user=${user}, home=${home}`);

  const target = await ensureWordpressFiles(home, user);
  const configPath = path.join(target, "wp-config.php");

  const contents = wpConfigSnippet({
    dbName,
    dbUser,
    dbPass,
    tablePrefix: tablePrefix || "wp_",
  });
  await writeFile(configPath, contents, "utf8");
  await exec("chown", [`${user}:${user}`, configPath]);
  await chmod(configPath, 0o640);
  const s = await stat(configPath);
  jstep("file-write", `Wrote ${configPath}`, {
    filePath: configPath,
    byteSize: s.size,
  });

  await exec("chown", ["-R", `${user}:${user}`, target]);
  jstep("shell", `Took ownership of ${target}`, {
    command: `chown -R ${user}:${user} ${target}`,
  });

  emit({
    ok: true,
    domain,
    home,
    user,
    target,
    configPath,
    installUrl: `https://${domain}/wp-admin/install.php`,
    adminUrl: `https://${domain}/wp-admin/`,
  });
}
